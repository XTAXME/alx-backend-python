def add(a: float, b: float) -> float:
    """
    Ajouter deux flottants et retourner la somme en tant que flottant.

    Args:
    a (float): Premier flottant à ajouter.
    b (float): Deuxième flottant à ajouter.

    Returns:
    float: La somme des deux flottants.
    """
    return a + b
