def concat(str1: str, str2: str) -> str:
    """
    Concaténer deux chaînes de caractères et retourner le résultat.

    Args:
    str1 (str): Première chaîne à concaténer.
    str2 (str): Deuxième chaîne à concaténer.

    Returns:
    str: La chaîne concaténée.
    """
    return str1 + str2
